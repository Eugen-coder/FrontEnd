/*

10. Сортировка чисел
Задание:
Дан массив numbers = [40, 10, 100, 30] . Отсортируй массив по возрастанию с
помощью метода sort .
Ожидаемый результат:
[10, 30, 40, 100]

*/

function sortN(a, b){
    if (a < b) return -1;
    if (a == b) return 0;
    if (a > b) return 1;
}
let numbersSort = [40, 10, 100, 30];
numbersSort.sort(sortN);
console.log(numbersSort);

