/*

19. Удаление всех чётных чисел
Задание:
Дан массив arr = [1, 2, 3, 4, 5, 6] . Удали все чётные числа, используя splice и цикл.
Ожидаемый результат:
[1, 3, 5]

*/

let arr4 = [1, 2, 3, 4, 5, 6];
let j = 0;

while (j < arr4.length) {
    if (arr4[j] % 2 === 0) {
        arr4.splice(j, 1);
    } else {
        j++;
    }
}
console.log(arr4);

